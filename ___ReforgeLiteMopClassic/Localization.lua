-- Early compatibility wrapper for CloseWindows.
do
    local addonTable = _G["ReforgeLiteMopClassic"]
    if type(addonTable) == "table" and type(addonTable.IsShown) ~= "function" then
        function addonTable:GetRealFrame()
            return self.ReforgeLiteMopClassic or self.frame or self.mainFrame
        end
        function addonTable:IsShown()
            local frame = self:GetRealFrame()
            if frame and type(frame.IsShown) == "function" then
                return frame:IsShown()
            end
            return false
        end
        function addonTable:Hide()
            local frame = self:GetRealFrame()
            if frame and type(frame.Hide) == "function" then
                return frame:Hide()
            end
        end
        function addonTable:Show()
            local frame = self:GetRealFrame()
            if frame and type(frame.Show) == "function" then
                return frame:Show()
            end
        end
    end
end
